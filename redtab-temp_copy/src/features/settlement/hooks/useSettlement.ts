export const useBulkDeleteRails = settlementRailHooks.useBulkDelete;

// ---------------------------------------------------------------------------
// Custom hooks — kept verbatim (domain-specific endpoints not covered by factory)
// ---------------------------------------------------------------------------

/**
 * Hook to fetch all settlement rails (returns plain array, not paginated).
 */
export function useSettlementRails(
  options?: Omit<UseQueryOptions<SettlementRail[]>, 'queryKey' | 'queryFn'>
) {
  return useQuery({
    queryKey: settlementKeys.rails(),
    queryFn: () => settlementApi.getAllRails(),
    ...options,
  });
}

/**
 * Hook to fetch filtered settlement rails with pagination and search.
 */
export function useFilteredSettlementRails(
  params?: SettlementRailsFilterParams,
  options?: Omit<UseQueryOptions<PaginatedResponse<SettlementRail>>, 'queryKey' | 'queryFn'>
) {
  return useQuery({
    queryKey: settlementKeys.railsFiltered(params),
    queryFn: () => settlementApi.getFilteredRails(params || {}),
    ...options,
  });
}

/**
 * Hook to fetch transactions for a rail.
 */
export function useRailTransactions(
  railId: string,
  options?: Omit<UseQueryOptions<RailTransaction[]>, 'queryKey' | 'queryFn'>
) {
  return useQuery({
    queryKey: settlementKeys.transactions(railId),
    queryFn: () => settlementApi.getRailTransactions(railId),
    enabled: !!railId,
    ...options,
  });
}

