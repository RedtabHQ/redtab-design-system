/**
 * Hook to select best rail for payment.
 * This doesn't cache as it's usually used for real-time decisions.
 */
export function useSelectBestRail(
  params: SelectBestRailParams,
  options?: Omit<UseQueryOptions<SettlementRail>, 'queryKey' | 'queryFn'>
) {
  return useQuery({
    queryKey: [...settlementKeys.all, 'select-best', params],
    queryFn: () => settlementApi.selectBestRail(params),
    enabled: !!params.amount,
    staleTime: 0, // Always fresh
    ...options,
  });
}

/**
 * Hook to health check a rail.
 */
export function useHealthCheckRail(
  options?: Omit<UseMutationOptions<{ status: string; latencyMs: number }, Error, string>, 'mutationFn'>
) {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: (railId: string) => settlementApi.healthCheck(railId),
    onSuccess: (_, railId) => {
      queryClient.invalidateQueries({ queryKey: settlementKeys.rail(railId) });
      queryClient.invalidateQueries({ queryKey: settlementKeys.rails() });
    },
    ...options,
  });
}

/**
 * Hook to fetch active bank providers (settlement rails) for dropdown selection.
 * Used in supplier onboarding to let users select their preferred bank/payment provider.
 */
export function useActiveBankProviders(
  options?: Omit<UseQueryOptions<SettlementRail[]>, 'queryKey' | 'queryFn'>
) {
  return useQuery({
    queryKey: [...settlementKeys.all, 'active-providers'],
    queryFn: async () => {
      const result = await settlementApi.getActiveRails();
      // Handle both direct array and paginated response
      if (Array.isArray(result)) {
        return result;
      }
      const paginatedResult = result as PaginatedResponse<SettlementRail>;
      return paginatedResult.items || [];
    },
    staleTime: 10 * 60 * 1000, // 10 minutes
    ...options,
  });
}
