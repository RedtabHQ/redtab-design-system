import { useQuery, useMutation, useQueryClient, type UseQueryOptions, type UseMutationOptions } from '@tanstack/react-query';
import { userApi, type UpdateUserSettingsData } from '@services';
import { createResourceHooks } from '@/hooks/createResourceHooks';
import { userService } from '@/lib/apiService';
import type { User, UserSettings } from '@types';
import type { PaginatedResponse } from '@types';

export interface UserListParams extends Record<string, unknown> {
  page?: number;
  pageSize?: number;
  search?: string;
  role?: string;
  isActive?: boolean;
  sortBy?: string;
  sortOrder?: 'asc' | 'desc';
}

/**
 * Create all user-related React Query hooks using the generic factory
 *
 * Standard CRUD hooks (useList, useDetail, useCreate, usePatch, useDelete, useBulkDelete)
 * are generated by the factory. Custom hooks (useMe, useUserSettings,
 * useUpdateUserSettings, useUpdateUser, useUploadAvatar) are kept below.
 */
const userCrud = createResourceHooks<User>('users', userService);

/**
 * Query keys for users — use for manual cache invalidation.
 * Extended with user-specific keys (me, settings) below.
 */
export const userKeys = {
  ...userCrud.keys,
  me: () => ['users', 'me'] as const,
  settings: (id: string) => ['users', 'settings', id] as const,
};

/**
 * Hook to fetch paginated list of users
 */
export const useUsers = (
  params?: UserListParams,
  options?: Omit<UseQueryOptions<PaginatedResponse<User>>, 'queryKey' | 'queryFn'>
) => userCrud.useList(params, options);

/**
 * Hook to fetch single user by ID
 */
export const useUser = userCrud.useDetail;

/**
 * Hook to create a new user
 */
export const useCreateUser = userCrud.useCreate;

/**
 * Hook to perform partial update of a user (PATCH)
 * For a full update that also invalidates the /me cache, use useUpdateUser.
 */
export const usePatchUser = userCrud.usePatch;

/**
 * Hook to delete a user
 */
export const useDeleteUser = userCrud.useDelete;

/**
 * Hook to bulk-delete users
 */
export const useBulkDeleteUsers = userCrud.useBulkDelete;

