/**
 * Hook to fetch current authenticated user profile from API
 *
 * This is the RECOMMENDED way to get current user data in the application.
 * It fetches fresh data from /users/me endpoint and caches it via React Query.
 *
 * @example
 * ```tsx
 * function UserProfile() {
 *   const { data: user, isLoading, error, refetch } = useMe();
 *
 *   if (isLoading) return <div>Loading...</div>;
 *   if (error) return <div>Error: {error.message} <button onClick={() => refetch()}>Retry</button></div>;
 *
 *   return <div>Hello, {user.username}!</div>;
 * }
 * ```
 */
export function useMe(
  options?: Omit<UseQueryOptions<User>, 'queryKey' | 'queryFn'>
) {
  return useQuery({
    queryKey: userKeys.me(),
    queryFn: () => userApi.me(),
    staleTime: 5 * 60 * 1000,
    gcTime: 10 * 60 * 1000,
    refetchOnMount: false,
    refetchOnWindowFocus: false,
    ...options,
  });
}

/**
 * Hook to fetch user settings
 */
export function useUserSettings(
  userId: string,
  options?: Omit<UseQueryOptions<UserSettings>, 'queryKey' | 'queryFn'>
) {
  return useQuery({
    queryKey: userKeys.settings(userId),
    queryFn: () => userApi.getUserSettings(userId),
    enabled: !!userId,
    ...options,
  });
}

/**
 * Hook to update user — also invalidates /me cache when the updated user
 * is the currently authenticated user.
 */
export function useUpdateUser(
  options?: Omit<UseMutationOptions<User, Error, { id: string; data: Parameters<typeof userApi.update>[1] }>, 'mutationFn'>
) {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: ({ id, data }: { id: string; data: Parameters<typeof userApi.update>[1] }) => userApi.update(id, data),
    onSuccess: (updatedUser) => {
      queryClient.invalidateQueries({ queryKey: userKeys.detail(updatedUser.id) });
      queryClient.invalidateQueries({ queryKey: userKeys.lists() });

      const currentUser = queryClient.getQueryData<User>(userKeys.me());
      if (currentUser && currentUser.id === updatedUser.id) {
        queryClient.invalidateQueries({ queryKey: userKeys.me() });
      }
    },
    ...options,
  });
}

/**
 * Hook to update user settings
 */
export function useUpdateUserSettings(
  options?: Omit<UseMutationOptions<UserSettings, Error, { userId: string; data: UpdateUserSettingsData }>, 'mutationFn'>
) {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: ({ userId, data }: { userId: string; data: UpdateUserSettingsData }) =>
      userApi.updateSettings(userId, data),
    onSuccess: (_, { userId }) => {
      queryClient.invalidateQueries({ queryKey: userKeys.settings(userId) });
    },
    ...options,
  });
}

/**
 * Hook to upload user avatar
 */
export function useUploadAvatar(
  options?: Omit<UseMutationOptions<User, Error, { userId: string; file: File }>, 'mutationFn'>
) {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: ({ userId, file }: { userId: string; file: File }) => userApi.uploadAvatar(userId, file),
    onSuccess: (_, { userId }) => {
      queryClient.invalidateQueries({ queryKey: userKeys.detail(userId) });
      queryClient.invalidateQueries({ queryKey: userKeys.me() });
    },
    ...options,
  });
}
